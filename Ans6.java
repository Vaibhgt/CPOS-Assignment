class Ans6
{
	public static void main(String args[])
	{
		int x=125;
		int y=24;
		
		//Sum
		int sum=x+y;
		System.out.println(x+" + "+y+" = "+sum);
		
		//Substraction
		int subs=x-y;
		System.out.println(x+" - "+y+" = "+subs);
		
		//Multiplication
		int multiply=x*y;
		System.out.println(x+" * "+y+" = "+multiply);
		
		//Division
		int division=x/y;
		System.out.println(x+" / "+y+" = "+division);
		
		// Modulus
		int modulus=x%y;
		System.out.println(x+" % "+y+" = "+modulus);
	}
}
		
		
		
		